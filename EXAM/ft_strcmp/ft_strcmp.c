int	ft_strcmp(char *s1, char *s2)
{
	int	n;
	int	i;

	i = 0;
	n = 0;
	if (*s2 == '\0')
		return (s1[i]);
	while (s1[i] != '\0' && s2[i] != '\0')
	{
		if (s1[i] != s2[i])
		{
			n = s1[i] - s2[i];
			return (n);
		}
		i++;
	}
	return (n);
}
